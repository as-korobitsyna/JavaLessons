public class PhoneBookEntry {

    String name;
    String surname;
    String email;
    String phonenumber;

    public PhoneBookEntry (String name, String surname, String email, String phonenumber) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.phonenumber = phonenumber;
    }
}
